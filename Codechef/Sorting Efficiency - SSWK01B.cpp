ll merge(vll &arr, vll &nyi, ll left, ll mid, ll right)
{
    ll i = left, j = mid, k = left, inv_count = 0;
    while (i < mid && j <= right) {
        if (arr[i] <= arr[j])
            nyi[k++] = arr[i++];
        else {
            nyi[k++] = arr[j++];
            inv_count += mid - i;
        }
    }

    while (i < mid)
        nyi[k++] = arr[i++];

    while (j <= right)
        nyi[k++] = arr[j++];

    for (i = left; i <= right; i++)
        arr[i] = nyi[i];
    
    return inv_count;
}

ll _mergesort(vll &arr, vll &nyi, ll left, ll right)
{
    ll inv_count = 0;
    if (left < right) {
        ll mid = (left + right) / 2;
        inv_count += _mergesort(arr, nyi, left, mid);
        inv_count += _mergesort(arr, nyi, mid + 1, right);
        inv_count += merge(arr, nyi, left, mid + 1, right);
    }
    return inv_count;
}

ll mergesort(vll arr)
{
    vll nyi(arr.size());
    return _mergesort(arr, nyi, 0, arr.size() - 1);
}

